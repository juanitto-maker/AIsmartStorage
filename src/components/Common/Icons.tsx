// Get icon component by file type
export function getFileTypeIcon(fileType: string): Component<IconProps> {
  switch (fileType) {
    case 'image':
      return ImageIcon;
    case 'video':
      return VideoIcon;
    case 'audio':
      return MusicIcon;
    case 'archive':
      return ArchiveIcon;
    case 'code':
      return CodeIcon;
    case 'document':
    case 'pdf':
    case 'spreadsheet':
    case 'presentation':
      return FileTextIcon;
    default:
      return FileIcon;
  }
}
